public class Card {
    //Attributes:
    //---------------
    private Integer cardValue;

    //Constructor:
    //---------------
    public Card(Integer cardValue) {
        this.cardValue = cardValue;
    }

    //Getters and Setters:
    //---------------
    public Integer getCardValue() {
        return cardValue;
    }

    public void setCardValue(Integer cardValue) {
        this.cardValue = cardValue;
    }
}
